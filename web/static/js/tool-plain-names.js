/**
 * Returns "toolName (plain-English meaning)" if a mapping exists,
 * otherwise just the tool name unchanged. Never renames the tool —
 * only appends a bracketed explanation for display.
 */
function withPlainToolName(toolName) {
    const plain = TOOL_PLAIN_NAMES[toolName];
    return plain ? `${toolName} (${plain})` : toolName;
}
